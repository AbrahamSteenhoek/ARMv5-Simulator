#include "testbarrelshifter.h"

testBarrelShifter::testBarrelShifter()
{

}

void testBarrelShifter::runTests()
{
    qInfo() << "testing Barrel Shifter...";

    assert(BarrelShifter::lsl(0b11111111111111111111111111111111, 4) == 0b11111111111111111111111111110000);
    assert(BarrelShifter::lsr(0b11111111111111111111111111111111, 4) == 0b00001111111111111111111111111111);
    assert(BarrelShifter::asr(0b10000000000000001111000000000000, 4) == 0b11111000000000000000111100000000);
    assert(BarrelShifter::ror(0b10000000000000001111000000000101, 4) == 0b01011000000000000000111100000000);

    //lsl
    assert(BarrelShifter::autoShift(0b00, 0b11111111111111111111111111111111, 4) == 0b11111111111111111111111111110000);
    //lsr
    assert(BarrelShifter::autoShift(0b01, 0b11111111111111111111111111111111, 4) == 0b00001111111111111111111111111111);
    //asr
    assert(BarrelShifter::autoShift(0b10, 0b10000000000000001111000000000000, 4) == 0b11111000000000000000111100000000);
    //ror
    assert(BarrelShifter::autoShift(0b11, 0b10000000000000001111000000000101, 4) == 0b01011000000000000000111100000000);

    qInfo() << "Barrel Shifter tests successful." << endl;
}
